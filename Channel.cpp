#include "Channel.hpp"

/*
** ------------------------------- CONSTRUCTOR --------------------------------
*/


Channel::Channel(void)
{
}

Channel::Channel(const Channel &src)
{
	if (this != &src)
		*this = src;
}


/*
** -------------------------------- DESTRUCTOR --------------------------------
*/

Channel::~Channel(void)
{
}


/*
** --------------------------------- OVERLOAD ---------------------------------
*/

Channel &Channel::operator=(Channel const &src)
{
	if (this == &src)
		return (*this);
	return (*this);
}

