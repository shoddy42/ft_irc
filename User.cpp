/*
** ------------------------------- CONSTRUCTOR --------------------------------
*/


User::User(void)
{
}

User::User(const User &src)
{
	if (this != &src)
		*this = src;
}


/*
** -------------------------------- DESTRUCTOR --------------------------------
*/

User::~User(void)
{
}


/*
** --------------------------------- OVERLOAD ---------------------------------
*/

User &User::operator=(User const &src)
{
	if (this == &src)
		return (*this);
	return (*this);
}

