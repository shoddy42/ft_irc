/*
** ------------------------------- CONSTRUCTOR --------------------------------
*/


Message::Message(void)
{
}

Message::Message(const Message &src)
{
	if (this != &src)
		*this = src;
}


/*
** -------------------------------- DESTRUCTOR --------------------------------
*/

Message::~Message(void)
{
}


/*
** --------------------------------- OVERLOAD ---------------------------------
*/

Message &Message::operator=(Message const &src)
{
	if (this == &src)
		return (*this);
	return (*this);
}

